public class Ex4App {

	public static void main(String[] args) {
		//DECLARACIÓN DE VARIABLES
		int n = 10;
		
		//MOSTRAR RESULTADOS
		System.out.println("Valor inicial de N = " + n);
		System.out.println(n + " + 77" + " = " + (n + 77));
		System.out.println(n + " - 3" + " = " + (n - 3));
		System.out.println(n + " * 2" + " = " + (n * 2));

	}

}
